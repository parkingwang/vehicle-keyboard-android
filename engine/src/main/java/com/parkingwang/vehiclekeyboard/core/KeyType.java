package com.parkingwang.vehiclekeyboard.core;

/**
 * 键位类型
 *
 * @author 陈哈哈 yoojiachen@gmail.com
 */
public enum KeyType {
    /**
     * 文本类型
     */
    GENERAL,

    /**
     * 功能键：删除
     */
    FUNC_DELETE,

    /**
     * 功能键：确定
     */
    FUNC_OK,

    /**
     * 功能键：更多
     */
    FUNC_MORE,

    /**
     * 功能键：返回
     */
    FUNC_BACK,
}
